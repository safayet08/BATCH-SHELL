# Export all locations as a combined hashtable
# Usage: . .\Locations.ps1 to dot-source and get $AllLocations
$AllLocations = @{
    "MKH4000" = $MKH4000
    "MKH4005" = $MKH4005
    "MKH4010" = $MKH4010
    "MKH4015" = $MKH4015
    "MKH4025" = $MKH4025
